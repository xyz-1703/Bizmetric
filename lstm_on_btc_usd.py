# Get the last timestamp from the fetched data
last_data_timestamp = df_btc.index[-1]

# Define 5 PM on the same day as the last data timestamp, in UTC
target_time = last_data_timestamp.replace(hour=17, minute=0, second=0, microsecond=0)

# If 5 PM today has already passed, set the target for 5 PM tomorrow
if target_time <= last_data_timestamp:
    target_time += datetime.timedelta(days=1)

# Calculate the number of hourly steps needed to reach the target time
# The model predicts the price for the hour *starting* at the predicted timestamp.
# If last data is 04:00, and target is 17:00, we need to predict 05:00, ..., 17:00. This is 13 predictions.
# So, the number of predictions is the difference in hours.
num_predictions_needed = int(np.ceil((target_time - last_data_timestamp).total_seconds() / 3600))

# Initialize with the last 'look_back' data points from the scaled_data
current_look_back_data = scaled_data[-look_back:]

predictions = []
prediction_timestamps = []

for i in range(num_predictions_needed):
    # Reshape the current look_back_data for prediction
    input_for_prediction = current_look_back_data.reshape(1, look_back, 1)

    # Make the prediction for the next hour (suppress verbose output for cleaner loop)
    predicted_scaled_value = model.predict(input_for_prediction, verbose=0)

    # Store the predicted scaled value
    predictions.append(predicted_scaled_value[0][0])

    # Calculate the timestamp for this prediction
    current_prediction_timestamp = last_data_timestamp + datetime.timedelta(hours=i + 1)
    prediction_timestamps.append(current_prediction_timestamp)

    # Update current_look_back_data: remove the oldest value and add the new prediction
    current_look_back_data = np.append(current_look_back_data[1:], predicted_scaled_value, axis=0)

# The final prediction is the last one made in the loop
final_predicted_scaled_value = predictions[-1].reshape(-1, 1)

# Inverse transform the final scaled prediction to get the actual price
predicted_price_5pm = scaler.inverse_transform(final_predicted_scaled_value)

